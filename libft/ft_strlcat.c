#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dstlen;
	size_t	srclen;

	i = 0;
	if ((!dst || !src) && size == 0)
		return (0);
	dstlen = ft_strlen((char *)dst);
	srclen = ft_strlen(src);
	if (size <= dstlen)
		return (srclen + size);
	while ((dstlen + i) < (size - 1) && src[i])
	{
		dst[dstlen + i] = src[i];
		i++;
	}
	dst[dstlen + i] = '\0';
	return (dstlen + srclen);
}
